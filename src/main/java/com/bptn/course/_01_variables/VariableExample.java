package com.bptn.course._01_variables;

public class VariableExample {
    public static void main(String[] args) {
        // Create a variable x of type int on the next line, and assign it the value of 43. After the first println, reassign its value to -143, then click the run button. 
    	 int x = 43; 
        
        // You should see two printed lines: one with your original variable, and one with the reassigned variable.
        
        // Declare and initialize the first value of x here.
        x = 43;
        
        // FREEZE CODE BEGIN
        System.out.println(x);
        // FREEZE CODE END

        // Reassign the value here 
        x = -143;
       
        
        // FREEZE CODE BEGIN
        System.out.println(x);
        
        // FREEZE CODE END
    }
}

    